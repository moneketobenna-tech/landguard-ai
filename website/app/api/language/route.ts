// Get translations with fallback to English
function getTranslations(lang: string): Record<string, string> {
  const translations = TRANSLATIONS[lang] || {}
  const english = TRANSLATIONS['en'] || {}
  
  // Merge with English as fallback
  return { ...english, ...translations }
}

export async function GET(request: NextRequest) {
  const { searchParams } = new URL(request.url)
  const lang = searchParams.get('lang')?.toLowerCase() || 'en'
  
  // Validate language
  const validLang = SUPPORTED_LANGUAGES.includes(lang) ? lang : 'en'
  const meta = LANGUAGE_META[validLang]
  const translations = getTranslations(validLang)
  
  return NextResponse.json({
    language: validLang,
    meta,
    supportedLanguages: SUPPORTED_LANGUAGES.map(code => ({
      code,
      ...LANGUAGE_META[code]
    })),
    translations,
  }, {
    headers: {
      'Cache-Control': 'public, max-age=86400', // Cache for 24 hours
    },
  })
}

